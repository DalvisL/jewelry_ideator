// MARK: - Generator

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export const DEFAULT_DOUBLE_INSPIRATION_CHANCE = 0.1;

// Each idea field, in display order. `on` is the default toggle state.
export const FIELDS = [
  { key: "type", label: "Piece", icon: "sparkles", on: true },
  { key: "metal", label: "Metal", icon: "hexagon", on: true },
  { key: "gemstone", label: "Gemstone", icon: "gem", on: true },
  { key: "style", label: "Style", icon: "palette", on: true },
  { key: "inspiration", label: "Inspiration", icon: "moon", on: true },
  { key: "setting", label: "Setting", icon: "wrench", on: false },
];

export const DEFAULT_FIELDS = Object.fromEntries(
  FIELDS.map((f) => [f.key, f.on])
);

export function generate({
  fields = DEFAULT_FIELDS,
  doubleInspirationChance = DEFAULT_DOUBLE_INSPIRATION_CHANCE,
} = {}) {
  let gemstone = null;
  let gemCut = null;
  let gemShape = null;
  if (fields.gemstone) {
    const useFaceted = Math.random() < 0.5;
    gemCut = useFaceted ? "Faceted" : "Cabochon";
    if (useFaceted) {
      gemstone = pick(facetedGemstones);
      gemShape = pick([...facetedShapes, ...sharedShapes]);
    } else {
      gemstone = pick(cabochonGemstones);
      gemShape = pick([...cabochonShapes, ...sharedShapes]);
    }
  }

  let inspiration = null;
  if (fields.inspiration) {
    inspiration = pick(inspirations);
    // Chance of a second inspiration (default ~1 in 10, configurable in Settings).
    if (Math.random() < doubleInspirationChance) {
      let second = pick(inspirations);
      while (second === inspiration) second = pick(inspirations);
      inspiration = `${inspiration} & ${second}`;
    }
  }

  return {
    id: crypto.randomUUID(),
    type: fields.type ? pick(jewelryTypes) : null,
    metal: fields.metal ? pick(metals) : null,
    gemstone,
    gemCut,
    gemShape,
    style: fields.style ? pick(styles) : null,
    inspiration,
    setting: fields.setting ? pick(settings) : null,
  };
}
